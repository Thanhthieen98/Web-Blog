package model;

public class Vote {
	private int id;
	private int vote;
	private New post;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getVote() {
		return vote;
	}
	public void setVote(int vote) {
		this.vote = vote;
	}
	public New getPost() {
		return post;
	}
	public void setPost(New post) {
		this.post = post;
	}
	
	
	
}
